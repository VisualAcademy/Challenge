// if 문으로 한번에 여러 개의 조건을 처리하기
#include <stdio.h>

int main(void)
{
    int number = 1234;

    //[A] 2개의 조건이 모두 만족하면
    if (number == 1234 && number >= 1000)
    {
        printf("맞습니다.\n");             // 실행
    }

    //[B] 2개의 조건 중 하나라도 만족하면
    if (number == 1234 || number <= 1000)
    {
        printf("하나라도 참이면 참\n");    // 실행
    }

    return 0;
}
